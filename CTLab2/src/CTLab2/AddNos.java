package CTLab2;
public class AddNos {

	/**
	 * This method takes input as arguments and returns the sum
	 * 
	 * @param x - accepts an integer value
	 * @param y - accepts an integer value
	 * @return - returns the sum of the two numbers as an integer
	 */
	int add(int x, int y)
	{
		int sum=0;
		sum=x+y;
		return sum;
	}

}
